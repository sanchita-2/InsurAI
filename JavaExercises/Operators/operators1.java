public class operators1 {
    
    public static void main(String[] args) {
        int a = 5;
        int b = 5;

        System.out.println("a + b = " + (a + b)); 
        System.out.println("a - b = " + (a - b)); 
        System.out.println("a * b = " + (a * b)); 
        System.out.println("a / b = " + (a / b)); 
        System.out.println("a % b = " + (a % b)); 
}
}
